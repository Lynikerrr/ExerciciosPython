# Faça um programa que leia a base e a altura e calcule as áreas do quadrado e do triangulo
base = float(input("Digite o valor da base: "))
alt = float(input("Digite o valor da altura: "))

area1 = base * alt
area2 = (base * alt) / 2

print("Area do quadrado: ", area1, " Area do triangulo: ", area2)
