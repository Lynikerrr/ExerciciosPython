# Crie um programa que leia 4 numeros reais do teclado e imprima a media entre eles 

num1 = float(input("Insira o primeiro numero: "))
num2 = float(input("Insira o segundo numero: "))
num3 = float(input("Insira o terceiro numero: "))
num4 = float(input("Insira o quarto numero: "))

med = (num1 + num2 + num3 + num4) / 4

print("A média dos numeros inseridos é: ", med)